/* retval is a bit vector with bit positions according to enum divBitPos */

Bool CEuInstallerStub_mdummy_Priv::m_CheckDVBDiversities(int country, int bitpos)
{
    Nat8 l_retval = 0x0;

    if ( (itvdiv_AtscDvbSupported != itvdiv_Off) && (amdivlib_brdcstdiv2_DvbTandCSupported != amdivlib_brdcstdiv2_Off) )
    {

        if ( itvdiv_DVBTInstallationSupported == itvdiv_On )
        {
            l_retval |= 0x1 << divDVBTFullBitPos; //set dvbt full bit in bit vector
        }
        else if ( itvdiv_DVBTInstallationSupported == itvdiv_CountryDependent )
        {
            // by default if a country is not listed here then it's a country which doesn't support DVBT!
            switch( country )
            {
                case cids_CountryAustria:
                case cids_CountryBelgium:
                case cids_CountryCzechrep:
                case cids_CountryDenmark:
                case cids_CountryFinland:
                case cids_CountryFrance:
                case cids_CountryGermany:
                case cids_CountryGreece:
                case cids_CountryItaly:
                case cids_CountryNetherlands:
                case cids_CountrySpain:
                case cids_CountrySweden:
                case cids_CountryUK:
                case cids_CountryAustralia:
                case cids_CountrySwitzerland:
                case cids_CountryNorway: //CR : brg36mgr#36101
                case cids_CountryEstonia: 
                case cids_CountryLithuania: 
                case cids_CountryLuxembourg:/*   brg36mgr#57075  */
                case cids_CountryHungary:   /*   brg36mgr#57054  */ 
                case cids_CountrySlovenia:   /*   brg36mgr#68735  */ 
                case cids_CountryPortugal:   /*   brg36mgr#76298  */ 
                case cids_CountryLatvia:
                case cids_CountrySlovakia:
                case cids_CountryCroatia:
                case cids_CountryIreland:
                case cids_CountryIsrael:
                case cids_CountryRussia:    /* brg36mgr#131775 */
                case cids_CountryMacedoniafyrom:
           		case cids_CountryBelarus: /* brg36mgr#137992 */
            	case cids_CountryUkraine:
            	case cids_CountryPoland:
				case cids_CountryBulgaria:	
				case cids_CountryAzerbaijan://CR:AN-24687	
				case cids_CountrySerbia:	/*AN-45722*/	
				case cids_CountryThailand: //APMEA Country
				case cids_CountryNewZealand: //APMEA Country
				case cids_CountrySingapore: //APMEA Country
				case cids_CountryMalaysia: //APMEA Country
				case cids_CountryTaiwan://APMEA Country
				case cids_CountryIndonesia: //APMEA Country
				case cids_CountryUAE:
				case cids_CountryVietnam:   //APMEA Country
                l_retval |= 0x1 << divDVBTFullBitPos;
                break;
            default:
                    if( itvdiv_DvbtLightSupported ) //check dvbt lite option
                    {
                        l_retval |= 0x1 << divDVBTLightBitPos;
                    }
                    else
                    {
                        l_retval = 0x0; //Analog only
                    }
                    break;
            }
        }
        else // DVBTInstallation is tvdiv_Off   
        {
            if( itvdiv_DvbtLightSupported ) //check dvbt lite option
            {
                l_retval |= 0x1 << divDVBTLightBitPos;
            }
            else
            {
                l_retval = 0x0; //both DVBT full and light are not supported - analog only
            }
        }

        if ( itvdiv_DVBCInstallationSupported == itvdiv_On )
        {
            l_retval |= 0x1 << divDVBCFullBitPos;
        }
        else if ( itvdiv_DVBCInstallationSupported == itvdiv_CountryDependent )
        {
            // by default if country is not listed here then it's a country which doesn't support DVBC!*/
            switch( country )
            {
                case cids_CountryFinland:
                case cids_CountrySweden:
                case cids_CountryNorway:
                case cids_CountryDenmark:
                case cids_CountryNetherlands:
                case cids_CountryGermany:
                case cids_CountryAustria:     //CR brg36mgr#74293 
                case cids_CountrySwitzerland: //CR brg36mgr#74293 
                case cids_CountryHungary:     //brg36mgr#102989
                case cids_CountryPoland:
                case cids_CountryRomania:
                case cids_CountryIreland:
				case cids_CountryBulgaria:
                case cids_CountryUkraine:
				case cids_CountryBelgium: //CR brg36mgr#244472
				case cids_CountrySlovenia:
                    l_retval |= 0x1 << divDVBCFullBitPos;
                    break;

                default:
                    if (itvdiv_DvbcLightSupported)
                    {
                        l_retval |= 0x1 << divDVBCLightBitPos;                  
                    }
                    else
                    {
                        //analog only if DVBT and DVBT light not supported
                    }
                    break;
            }
        }
        else // itvdiv_DVBCInstallationSupported == itvdiv_Off
        {
            if (itvdiv_DvbcLightSupported)
            {
                l_retval |= 0x1 << divDVBCLightBitPos;
            }
            else
            {
                //analog only if DVBC and DVBC light not supported
            }
        }

        if(amdivlib_brdcstdiv_DvbT2InstallationSupported != amdivlib_brdcstdiv_Off)
        {
          l_retval |= 0x1 << divDvbT2BitPos;
        }

    }
    else
    {
        l_retval = 0x0; //both DVBT full and light are not supported - analog only
    }
    //m_DVBBitVector = l_retval;
    return ((Bool) ((l_retval >>  bitpos ) & 0x1));
}

